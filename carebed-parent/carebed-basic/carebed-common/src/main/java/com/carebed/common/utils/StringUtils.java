    public static boolean isIDNumber(String IDNumber) {
        if (IDNumber == null || "".equals(IDNumber)) {
            return false;
        }
        // 定义判别用户身份证号的正则表达式（15位或者18位，最后一位可以为字母）
        String regularExpression = "(^[1-9]\\d{5}(18|19|20)\\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\\d{3}[0-9Xx]$)|" +
                "(^[1-9]\\d{5}\\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\\d{3}$)";

        boolean matches = IDNumber.matches(regularExpression);

        //判断第18位校验值
        if (matches) {
            if (IDNumber.length() == 18) {
                try {
                    char[] charArray = IDNumber.toCharArray();
                    //前十七位加权因子
                    int[] idCardWi = {7, 9, 10, 5, 8, 4, 2, 1, 6, 3, 7, 9, 10, 5, 8, 4, 2};
                    //这是除以11后，可能产生的11位余数对应的验证码
                    String[] idCardY = {"1", "0", "X", "9", "8", "7", "6", "5", "4", "3", "2"};
                    int sum = 0;
                    for (int i = 0; i < idCardWi.length; i++) {
                        int current = Integer.parseInt(String.valueOf(charArray[i]));
                        int count = current * idCardWi[i];
                        sum += count;
                    }
                    char idCardLast = charArray[17];
                    int idCardMod = sum % 11;
                    if (idCardY[idCardMod].toUpperCase().equals(String.valueOf(idCardLast).toUpperCase())) {
                        return true;
                    } else {
                        System.out.println("身份证号："+IDNumber+",最后一位:" + String.valueOf(idCardLast).toUpperCase() +
                                "错误,正确的应该是:" + idCardY[idCardMod].toUpperCase());
                        return false;
                    }

                } catch (Exception e) {
                    e.printStackTrace();
                    System.out.println("异常:" + IDNumber);
                    return false;
                }
            }

        }
        return matches;
    }

    /**
     * 阿拉伯数据转中文数字
     * @param str
     * @return
     */
    public static String NumToZHStr(String str) {
        //定义转换后显示数组
        String[] alabo = { "零", "一", "二", "三", "四", "五", "六", "七", "八", "九" };
        String[] zwhz = { "十", "百", "千", "万", "十", "百", "千", "亿", "十", "百", "千" };
        String digital = "";
        int n = str.length();
        for (int i = 0; i < n; i++) {
            int o = str.charAt(i) - '0';
            if (i != n - 1 && o != 0) {
                String aa = alabo[o];
                String bb = zwhz[n - 2 - i];
                if("一".equals(aa) && "十".equals(bb)){
                    aa = "";
                }
                digital += aa + bb;
            } else {
                if(!"零".equals(alabo[o])){
                    digital += alabo[o];
                }
            }
        }
        return digital;
    }

    /**
     * @Description: 字符转换方法
     * @Author: GDJ
     * @Date: 2020/06/02
     * @param orig:
     * @return: java.lang.String
     **/
    public static String convert(Object orig) {
        if (orig == null) {
            return null;
        }
        if (orig instanceof String) {
            return (String) orig;
        }
        if (orig instanceof Short) {
            return Short.toString((Short) orig);
        }
        if (orig instanceof Integer) {
            return Integer.toString((Integer) orig);
        }
        if (orig instanceof Long) {
            return Long.toString((Long) orig);
        }
        if (orig instanceof Float) {
            return Float.toString((Float) orig);
        }
        if (orig instanceof Double) {
            return Double.toString((Double) orig);
        }
        if (orig instanceof Boolean) {
            return Boolean.toString((Boolean) orig);
        }
        SimpleDateFormat format = new SimpleDateFormat("yyyyMMddHHmmss");
        if (orig instanceof java.util.Date) {
            return format.format((java.util.Date) orig);
        }
        if (orig instanceof java.sql.Date) {
            return format.format((java.sql.Date) orig);
        }
        if (orig instanceof java.util.Calendar) {
            return format.format(((java.util.Calendar) orig).getTime());
        }
        throw new IllegalArgumentException("参数类型不支持.");
    }
